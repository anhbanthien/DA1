    private void showData(List<HoaDonModel> lists) {
        dtm.setRowCount(0);
        for (HoaDonModel hd : lists) {
            dtm.addRow(hd.toDataRow());
        }
    }

    private void showDataSP(List<QLHDCT> listsp) {
        dtm1.setRowCount(0);
        for (QLHDCT sp : listsp) {
            dtm1.addRow(sp.toDataRow());
        }
    }

