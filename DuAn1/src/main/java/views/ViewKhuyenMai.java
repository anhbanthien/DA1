    private void fillToTable(List<KhuyenMai> km) {
        dtm.setRowCount(0);
        for (KhuyenMai o : km) {
            dtm.addRow(new Object[]{o.getMaKM(), o.getNgayBatDau(), o.getNgayKetThuc(), o.getPhanTramKM(), o.getTrangThai() == 1 ? "Còn hoạt động" : "Ngưng hoạt động"});
        }

    }

